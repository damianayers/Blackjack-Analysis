def get_player_decision(player_total, dealer_up_card, is_soft, is_double, dealer_hits_on_soft_17=False):
    if is_double:
        return get_player_doubles_hand_decision(player_total, dealer_up_card - 2, dealer_hits_on_soft_17)
    elif is_soft:
        return get_player_soft_hand_decision(player_total, dealer_up_card - 2, dealer_hits_on_soft_17)
    else:
        return get_player_hard_hand_decision(player_total, dealer_up_card - 2, dealer_hits_on_soft_17)


